	#define NUM_OCTAVES 5

float fbm(vec3 x) {
	float v = 0.0;
	float a = 0.5;
	vec3 shift = vec3(100);
	for (int i = 0; i < NUM_OCTAVES; ++i) {
		v += a * snoise(vec4(x,time));
		x = x * 2.0 + shift;
		a *= 0.5;
	}
	return v;
}

attribute vec3 aPosition;
varying float vNoise;
uniform float uFrequency;
uniform float uAmplitude;

void main() {

	vNoise = fbm(aPosition*uFrequency);

	//normal * scalar noise
	vec3 newPosition = normalize(aPosition) * (1.0 + uAmplitude * vNoise);

	//Point size - snoise
	gl_PointSize = 1.5;
	gl_Position = projectionMatrix * modelViewMatrix * vec4( newPosition, 1.0 );
}
